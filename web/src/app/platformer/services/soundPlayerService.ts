import { Injectable } from "@angular/core";
import * as p5 from "p5";
import "p5/lib/addons/p5.sound";

export interface SoundFiles {
  click: p5.SoundFile;
  pause: p5.SoundFile;
  jump: p5.SoundFile;
  land: p5.SoundFile;
}

/**
 * Service to play sounds
 */
@Injectable()
export class SoundPlayerService {
  private _soundFiles: SoundFiles;

  public set soundFiles(val: SoundFiles) {
    this._soundFiles = val;
  }

  public playClick(): void {
    this._soundFiles?.click.play();
  }

  public playPause(): void {
    this._soundFiles?.pause.play();
  }

  public playJump(): void {
    this._soundFiles?.jump.play();
  }

  public playLand(): void {
    this._soundFiles?.land.play();
  }
}
